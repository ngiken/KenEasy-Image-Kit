  function createI18n(config) {
    var current = detectLanguage(config.storageKeys.language);

    function detectLanguage(key) {
      try {
        var saved = localStorage.getItem(key);
        if (saved === "zh" || saved === "en") return saved;
      } catch (error) {}
      return (navigator.language || "en").toLowerCase().indexOf("zh") === 0 ? "zh" : "en";
    }

    function translate(key, variables) {
      var dictionary = COPY[current] || COPY.en;
      var value = dictionary[key];
      if (value == null) value = COPY.en[key] != null ? COPY.en[key] : key;
      if (!variables) return value;
      return value.replace(/\{(\w+)\}/g, function (_, name) {
        return variables[name] != null ? variables[name] : "{" + name + "}";
      });
    }

    function setLanguage(next) {
      if (next !== "zh" && next !== "en") return;
      current = next;
      try {
        localStorage.setItem(config.storageKeys.language, next);
      } catch (error) {}
    }

    return {
      getLanguage: function () { return current; },
      setLanguage: setLanguage,
      t: translate,
    };
  }

  global.KenEasyImageKitI18n = {
    copy: COPY,
    create: createI18n,
  };
